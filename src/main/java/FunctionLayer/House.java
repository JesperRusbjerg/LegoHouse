package FunctionLayer;


public class House {

private int x4Brick;
private int x2Brick;
private int x1Brick;

    public House(int x4Brick, int x2Brick, int x1Brick) {
        this.x4Brick = x4Brick;
        this.x2Brick = x2Brick;
        this.x1Brick = x1Brick;
    }

    public int getX4Brick() {
        return x4Brick;
    }

    public int getX2Brick() {
        return x2Brick;
    }

    public int getX1Brick() {
        return x1Brick;
    }



    

   

   



    
}
